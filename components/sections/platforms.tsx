export function PlatformsSection() {
  const sectionRef = useRef<HTMLElement>(null)
  const [isVisible, setIsVisible] = useState(false)

  useEffect(() => {
    const observer = new IntersectionObserver(
      ([entry]) => {
        if (entry.isIntersecting) {
          setIsVisible(true)
        }
      },
      { threshold: 0.2 }
    )

    if (sectionRef.current) {
      observer.observe(sectionRef.current)
    }

    return () => observer.disconnect()
  }, [])

  return (
    <section
      ref={sectionRef}
      className="py-24 sm:py-32"
    >
      <div className="mx-auto max-w-5xl px-4 sm:px-6 lg:px-8 text-center">
        <h2
          className={`font-serif text-3xl sm:text-4xl font-bold text-foreground mb-12 transition-all duration-700 ${
            isVisible ? "opacity-100 translate-y-0" : "opacity-0 translate-y-8"
          }`}
        >
          Găsești AI AFLAT pe
        </h2>

        <div className="grid grid-cols-2 sm:grid-cols-4 gap-4">
          {platforms.map((platform, index) => (
            <a
              key={platform.name}
              href="#"
              className={`group flex flex-col items-center gap-3 p-6 rounded-xl bg-navy-light hover:bg-navy-lighter border border-border/50 hover:border-cyan/30 transition-all duration-300 hover:glow-cyan ${
                isVisible
                  ? "opacity-100 translate-y-0"
                  : "opacity-0 translate-y-8"
              }`}
              style={{ transitionDelay: `${index * 75}ms` }}
            >
              <span className="text-muted-foreground group-hover:text-cyan transition-colors">
                <PlatformIcon icon={platform.icon} />
              </span>
              <span className="text-sm font-medium text-muted-foreground group-hover:text-foreground transition-colors">
                {platform.name}
              </span>
            </a>
          ))}
        </div>
      </div>
    </section>
  )
}
